# Print a string
print("Hello, world!")

# Print quotation marks
print('She said "Hello" and then left.')
print("She said \"Hello\" and then left.")

# Print multiple lines of string
print("Hello, world!\nHello, world!")

# Concatenate strings
print("Hello, " + "Ben!")

# Get user input
input("What is your name? ")

# Get the length of a string
print(len("Hello!"))

# Save data in a variable
name = "Jack"
print(name)

# Change variable value
name = "Tom"
print(name)

# Multi-word variable name
user_name = "admin"
print(user_name)
