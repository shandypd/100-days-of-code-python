# Conditionals
water_level = 71
if water_level > 80:
    print("Drain water")
elif water_level > 70:
    print("Almost full")
else:
    print("Continue")

# Logical operators
print(True and False)
print(False or True)
print(not True)

# Count occurences of a character in a string
print("hello".count("l"))

# Convert a string to lowercase
print("HelLo".lower())

# Convert a string to uppercase
print("heLlo".upper())

# Print multi-line strings
print("""This is the first line
And this is the second line""")
